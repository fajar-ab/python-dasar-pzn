a = 10
b = 3

print(a + b) # penjumlahan
print(a - b) # pengurangan
print(a * b) # perkalian
print(a / b) # pembagian

print(a // b) # pembagian bulat tanpa desimal
print(a % b)  # sisa bagi (modulo)

# contoh praktis modulo
print(10 % 2) # 0 (10 habis dibagi 2)
print(11 % 2) # 1 (sisa 1 ketika 11 dibagi 2)

a = 2
b = 3

print(a ** b) # (2 pangkat 3)

# contoh lain
print(5 ** 2) # 25 (5 pangkat 2)
print(3 ** 4) # 81 (3 pangkat 4)