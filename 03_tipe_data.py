# Type data Float

# bilangan dengan koma
tinggi = 175.5
berat = 80.2
pi = 3.141592653589793
suhu = -30.0

# notasi sientific
speed_of_light = 3e8    # 3 x 10^8 = 300000000.0
verry_small = 1e-6      # 0.000001

print(type(pi))
print(speed_of_light)


